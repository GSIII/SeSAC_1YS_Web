package sesac.spring_0311.UserVO;

public class InfoVO {
    private String name;
    private String gender;
    private String date;
    private String interest;

    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public String getDate() {
        return date;
    }

    public String getInterest() {
        return interest;
    }
}
